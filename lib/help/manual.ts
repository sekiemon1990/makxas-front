export const HELP_CATEGORY_LABELS: Record<HelpChapter["category"], string> = {
  overview: "はじめに",
  inquiry: "反響対応",
  ai: "AI機能",
  leads: "リード管理",
  admin: "管理者向け",
};

export function findChapterByPath(currentPath: string): HelpChapter | null {
  for (const ch of HELP_CHAPTERS) {
    if (!ch.path) continue;
    if (ch.path === currentPath) return ch;
    const pattern = ch.path.replace(/\[[^\]]+\]/g, "[^/]+");
    if (new RegExp(`^${pattern}$`).test(currentPath)) return ch;
  }
  return null;
}

export function searchChapters(query: string): HelpChapter[] {
  const q = query.toLowerCase().trim();
  if (!q) return HELP_CHAPTERS;
  return HELP_CHAPTERS.filter((ch) => {
    if (ch.title.toLowerCase().includes(q)) return true;
    if (ch.tags.some((t) => t.toLowerCase().includes(q))) return true;
    if (ch.content.toLowerCase().includes(q)) return true;
    return false;
  });
}

/**
 * AI チャットの system prompt 用に、全ヘルプ章をコンパクトな形式で連結。
 * 引用ルールを末尾に付与し、AI が回答時に [help: <id>] 形式で参照できるよう指示。
 */
export function buildHelpDocsContext(): string {
  const sections = HELP_CHAPTERS.map(
    (ch) =>
      `### ${ch.title} (id: ${ch.id})\n${ch.path ? `関連画面: ${ch.path}\n` : ""}${ch.content}`,
  ).join("\n\n---\n\n");

  return `\n\n## システムのヘルプドキュメント (操作方法・機能説明)

ユーザーから「○○ってどうやって使うの？」「××の機能は？」のような操作・機能に関する質問を受けた場合は、以下のドキュメントを参照して回答してください。
回答時は該当章のタイトルを引用し、末尾に「📖 詳しくは [<章タイトル>](/help?chapter=<章id>) を参照」のリンクを付けてください。

${sections}
`;
}
